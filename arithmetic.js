// Arithmetic Operators:Arithmetic Operators are used to perform arithmetic on numbers to return numeric results.

// What arithmetic operators are available on JavaScript?JavaScript arithmetic operators are addition (+), subtraction (-), multiplication (*), division (/), modulus (%) increment, decrement, and expontiation

// addition (+)
let a = 2 + 2;
console.log(a);

//subtraction (-)
let b = 4 - 2;
console.log(b);

//multiplication (*)
let c = 2 * 2;
console.log(c);

//division (/)
let d = 4 / 2;
console.log(d);

// modulus (%) 
let e = 10 % 2;
console.log(e);

let f = 11 % 2;
console.log(f);

//raise to the power. multiplication (*)
let g = 3 ** 2;
console.log(g);


//increment and decrement
let h = 10;
h = h + 1;
h += 1;
h++;
++h;
h -= 1;
h--;
--h;
console.log(h);